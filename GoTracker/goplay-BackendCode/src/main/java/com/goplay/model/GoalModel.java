package com.goplay.model;


/**
 * The Class GoalModel.
 */
public class GoalModel {

	/** The goal id. */
	int goal_id;
	
	/** The category. */
	String category;
	
	/** The user id. */
	String user_id;
	
	/** The start date. */
	String start_date;
	
	/** The description. */
	String description;
	
	/** The no of days. */
	int no_of_days;
	
	/**
	 * Gets the category.
	 *
	 * @return the category
	 */
	public String getCategory() {
		return category;
	}
	
	/**
	 * Sets the category.
	 *
	 * @param category
	 *            the new category
	 */
	public void setCategory(String category) {
		this.category = category;
	}
	
	/**
	 * Gets the user id.
	 *
	 * @return the user id
	 */
	public String getUser_id() {
		return user_id;
	}
	
	/**
	 * Sets the user id.
	 *
	 * @param user_id
	 *            the new user id
	 */
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	
	/**
	 * Gets the start date.
	 *
	 * @return the start date
	 */
	public String getStart_date() {
		return start_date;
	}
	
	/**
	 * Sets the start date.
	 *
	 * @param start_date
	 *            the new start date
	 */
	public void setStart_date(String start_date) {
		this.start_date = start_date;
	}
	
	/**
	 * Gets the description.
	 *
	 * @return the description
	 */
	public String getDescription() {
		return description;
	}
	
	/**
	 * Sets the description.
	 *
	 * @param description
	 *            the new description
	 */
	public void setDescription(String description) {
		this.description = description;
	}
	
	/**
	 * Gets the no of days.
	 *
	 * @return the no of days
	 */
	public int getNo_of_days() {
		return no_of_days;
	}
	
	/**
	 * Sets the no of days.
	 *
	 * @param no_of_days
	 *            the new no of days
	 */
	public void setNo_of_days(int no_of_days) {
		this.no_of_days = no_of_days;
	}
	
	/**
	 * Gets the goal id.
	 *
	 * @return the goal id
	 */
	public int getGoal_id() {
		return goal_id;
	}
	
	/**
	 * Sets the goal id.
	 *
	 * @param goal_id
	 *            the new goal id
	 */
	public void setGoal_id(int goal_id) {
		this.goal_id = goal_id;
	}
	
	
	
}
