package com.goplay.model;


/**
 * The Class User.
 */
public class User {

	/** The id. */
	int id;
	
	/** The facebook id. */
	String facebook_id;
	
	/** The email. */
	String email;
	
	/** The name. */
	String name;
	
	/** The date of birth. */
	String date_of_birth;
	
	/** The gender. */
	String gender;
	
	/**
	 * Gets the facebook id.
	 *
	 * @return the facebook id
	 */
	public String getFacebook_id() {
		return facebook_id;
	}
	
	/**
	 * Sets the facebook id.
	 *
	 * @param facebook_id
	 *            the new facebook id
	 */
	public void setFacebook_id(String facebook_id) {
		this.facebook_id = facebook_id;
	}
	
	/**
	 * Gets the email.
	 *
	 * @return the email
	 */
	public String getEmail() {
		return email;
	}
	
	/**
	 * Sets the email.
	 *
	 * @param email
	 *            the new email
	 */
	public void setEmail(String email) {
		this.email = email;
	}
	
	/**
	 * Gets the name.
	 *
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	
	/**
	 * Sets the name.
	 *
	 * @param name
	 *            the new name
	 */
	public void setName(String name) {
		this.name = name;
	}
	
	/**
	 * Gets the date of birth.
	 *
	 * @return the date of birth
	 */
	public String getDate_of_birth() {
		return date_of_birth;
	}
	
	/**
	 * Sets the date of birth.
	 *
	 * @param date_of_birth
	 *            the new date of birth
	 */
	public void setDate_of_birth(String date_of_birth) {
		this.date_of_birth = date_of_birth;
	}
	
	/**
	 * Gets the gender.
	 *
	 * @return the gender
	 */
	public String getGender() {
		return gender;
	}
	
	/**
	 * Sets the gender.
	 *
	 * @param gender
	 *            the new gender
	 */
	public void setGender(String gender) {
		this.gender = gender;
	}
	
	
	/**
	 * Gets the id.
	 *
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	
	/**
	 * Sets the id.
	 *
	 * @param id
	 *            the new id
	 */
	public void setId(int id) {
		this.id = id;
	}
	
	
	
	
}
